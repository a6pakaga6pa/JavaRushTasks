package com.javarush.task.task15.task1507;

/* 
ООП - Перегрузка
*/

public class Solution {
    public static void main(String[] args) {
        printMatrix(2, 3, "8");
    }

    public static void printMatrix(int m, int n, String value) {
        System.out.println("Заполняем объектами String");
        printMatrix(m, n, (Object) value);
    }

    public static void printMatrix(int m, int n, Object value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
            printMatrix((Integer)m, (Integer)n, (String) value);
        }
    }

    public static void printMatrix(Integer m, Integer n, String value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
            printMatrix((double)m, (double)n, value);
        }
    }

    public static void printMatrix(double m, double n, String value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
            printMatrix(m, (int)n, value);
        }
    }

    public static void printMatrix(double m, int n, String value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
            printMatrix((float)m, (char) n, (Object) value);

        }
    }

    public static void printMatrix(float m, char n, Object value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
            printMatrix((long)m, (byte) n, value);
        }
    }

    public static void printMatrix(long m, byte n, Object value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
            printMatrix((double)m, (char) n, value);
        }
    }

    public static void printMatrix(double m, char n, Object value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
            printMatrix((char)m, (float) n, (String)value);
        }
    }

    public static void printMatrix(char m, float n, String value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
            printMatrix((Character) m, n, value);
        }
    }

    public static void printMatrix(Character m, float n, String value) {
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(value);
            }
            System.out.println();
        }
    }
}
